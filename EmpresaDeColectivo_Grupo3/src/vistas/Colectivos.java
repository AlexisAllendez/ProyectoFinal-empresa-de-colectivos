    private Integer getIdTabla(int fila) {
        Object val = jtTabla.getValueAt(fila, 0);
        if (val == null) {
            return null;
        }
        if (val instanceof Integer) {
            return (Integer) val;
        }
        return Integer.valueOf((String) val);
    }
